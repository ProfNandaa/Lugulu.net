<h1>About Lugulu.net</h1>

<p>This is a not-for-profit initiative that was started by a group of young people
from Lugulu village, basically aimed at sharing of information and ideas and networking
of the villagers of Lugulu.</p>

<h3>Development Stage</h3>
<p>Currently this web system is under <b>slow but steady</b> construction by a well able team of developers
from the village, with the support of <a href="http://webtribe.co.ke" target="_blank">Web Tribe</a> as 
the major sponsors of the project</p>

