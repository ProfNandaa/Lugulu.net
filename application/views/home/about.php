<h3>Our Appeal</h3>
<p><b>To Groups and CBOS</b></p>
<p>We are requesting for all those who have women/men/youth groups, CBOs, and NGOs in the <b>Lugulu area</b> to submit their information to info@lugulu.net for publishing on the website. Please note that this can go a long way in increasing your visibility.</p>

<p><b>To Churches</b></p>
<p>Churches too are welcome to send their church profiles for publishing on the website.</p>

<p><b>To the rest of the villagers</b></p>
<p>We are asking for your ideas on how we can make this project better and bigger.</p>
<p>For those that can blog/write about anything informative, we want to give you space on the village's blog, <a href="http://blog.lugulu.net" target="_blank">blog.lugulu.net</a>, please get in touch.</p>


